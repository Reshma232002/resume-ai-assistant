import re
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.metrics.pairwise import cosine_similarity

# ============================
# 1. FIXED SKILL DATABASE
# ============================
SKILLS_DB = [
    "python", "numpy", "pandas", "scikit-learn",
    "tensorflow", "pytorch",
    "machine learning", "deep learning",
    "data analysis", "data cleaning",
    "feature engineering",
    "sql", "power bi", "tableau",
    "linux", "aws", "splunk",
    "excel", "statistics",
    "api", "automation", "git"
]

# ============================
# 2. CLEAN TEXT FUNCTION
# ============================
def clean(text):
    text = text.lower()
    text = re.sub(r'[^a-zA-Z0-9 ]', ' ', text)
    text = re.sub(r'\s+', ' ', text)
    return text

# ============================
# 3. SKILL EXTRACTION FUNCTION
# ============================
def extract_skills(resume_text, jd_text):

    resume_text = clean(resume_text)
    jd_text = clean(jd_text)

    matched = []
    missing = []

    for skill in SKILLS_DB:
        if skill in resume_text and skill in jd_text:
            matched.append(skill)
        elif skill in jd_text and skill not in resume_text:
            missing.append(skill)

    return matched, missing

# ============================
# 4. MAIN ANALYSIS FUNCTION
# ============================
def analyze_resume(resume_text, job_description):

    resume_clean = clean(resume_text)
    jd_clean = clean(job_description)

    # ----------------------------
    # ATS SCORE (TF-IDF)
    # ----------------------------
    vectorizer = TfidfVectorizer()
    vectors = vectorizer.fit_transform([resume_clean, jd_clean])

    similarity = cosine_similarity(vectors[0:1], vectors[1:2])[0][0]
    ats_score = round(similarity * 100, 2)

    # ----------------------------
    # SKILL MATCHING (FIXED)
    # ----------------------------
    matched, missing = extract_skills(resume_text, job_description)

    # ----------------------------
    # OUTPUT
    # ----------------------------
    return {
        "score": ats_score,
        "matched": matched,
        "missing": missing,

        "cover_letter": "Generated by AI module",
        "linkedin_summary": "Generated by AI module",

        "interview_questions": [
            "Explain your experience in Linux troubleshooting",
            "How do you handle system incidents?",
            "What automation scripts have you written?"
        ]
    }
