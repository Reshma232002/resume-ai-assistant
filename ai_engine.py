import re
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.metrics.pairwise import cosine_similarity

# ============================
# SKILL DATABASE (REAL SKILLS ONLY)
# ============================
SKILLS_DB = {
    "python", "numpy", "pandas", "scikit-learn",
    "tensorflow", "pytorch",
    "machine learning", "deep learning",
    "data analysis", "data cleaning",
    "feature engineering",
    "sql", "power bi", "tableau",
    "linux", "aws", "splunk",
    "excel", "api", "automation"
}

# ============================
# CLEAN TEXT
# ============================
def clean(text):
    text = text.lower()
    text = re.sub(r'[^a-zA-Z0-9 ]', ' ', text)
    text = re.sub(r'\s+', ' ', text)
    return text

# ============================
# SKILL MATCHING
# ============================
def extract_skills(resume_text, jd_text):

    resume = clean(resume_text)
    jd = clean(jd_text)

    matched = []
    missing = []

    for skill in SKILLS_DB:
        if skill in resume and skill in jd:
            matched.append(skill)
        elif skill in jd and skill not in resume:
            missing.append(skill)

    return matched, missing

# ============================
# REASONING ENGINE (NEW)
# ============================
def generate_reasoning(score, matched, missing):

    # Strength logic
    if score >= 70:
        level = "Strong Match"
        summary = "Your profile aligns well with the job requirements."
    elif score >= 40:
        level = "Moderate Match"
        summary = "Your profile partially matches the job requirements."
    else:
        level = "Low Match"
        summary = "Your profile needs significant improvement for this role."

    # Strengths
    strengths = matched[:5] if matched else ["Basic technical exposure"]

    # Gaps
    gaps = missing[:5] if missing else ["No major gaps detected"]

    # Recommendation logic
    if len(missing) > len(matched):
        recommendation = "Focus on learning missing core technical skills to improve job fit."
    else:
        recommendation = "Strengthen advanced skills and real-world project experience."

    return {
        "level": level,
        "summary": summary,
        "strengths": strengths,
        "gaps": gaps,
        "recommendation": recommendation
    }

# ============================
# MAIN FUNCTION
# ============================
def analyze_resume(resume_text, job_description):

    resume_clean = clean(resume_text)
    jd_clean = clean(job_description)

    # ATS SCORE
    vectorizer = TfidfVectorizer()
    vectors = vectorizer.fit_transform([resume_clean, jd_clean])

    similarity = cosine_similarity(vectors[0:1], vectors[1:2])[0][0]
    score = round(similarity * 100, 2)

    # SKILLS
    matched, missing = extract_skills(resume_text, job_description)

    # REASONING LAYER (NEW)
    reasoning = generate_reasoning(score, matched, missing)

    return {
        "score": score,
        "matched": matched,
        "missing": missing,

        # NEW HACKATHON LAYER
        "reasoning": reasoning,

        "cover_letter": "Generated by AI module",
        "linkedin_summary": "Generated by AI module",

        "interview_questions": [
            "Explain your experience in Linux troubleshooting",
            "How do you handle system incidents?",
            "What automation scripts have you written?"
        ]
    }
