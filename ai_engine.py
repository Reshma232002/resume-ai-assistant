import re
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.metrics.pairwise import cosine_similarity

def analyze_resume(resume_text, job_description):

    resume_text = resume_text.lower()
    job_description = job_description.lower()

    # ----------------------------
    # 1. CLEAN TEXT
    # ----------------------------
    def clean(text):
        text = re.sub(r'[^a-zA-Z0-9 ]', ' ', text)
        text = re.sub(r'\s+', ' ', text)
        return text

    resume_clean = clean(resume_text)
    jd_clean = clean(job_description)

    # ----------------------------
    # 2. SEMANTIC SIMILARITY SCORE
    # ----------------------------
    vectorizer = TfidfVectorizer()
    vectors = vectorizer.fit_transform([resume_clean, jd_clean])

    similarity = cosine_similarity(vectors[0:1], vectors[1:2])[0][0]
    ats_score = round(similarity * 100, 2)

    # ----------------------------
    # 3. KEYWORD MATCHING (IMPROVED)
    # ----------------------------
    jd_keywords = set(jd_clean.split())
    resume_keywords = set(resume_clean.split())

    matched = list(jd_keywords & resume_keywords)
    missing = list(jd_keywords - resume_keywords)

    # remove noise words
    stop_words = {"and", "or", "the", "to", "of", "in", "a", "for", "on", "with"}

    matched = [w for w in matched if w not in stop_words]
    missing = [w for w in missing if w not in stop_words]

    # ----------------------------
    # 4. SIMPLE OUTPUT GENERATION
    # ----------------------------
    return {
        "score": ats_score,
        "matched": matched[:15],
        "missing": missing[:15],
        "cover_letter": "Generated by AI module",
        "linkedin_summary": "Generated by AI module",
        "interview_questions": [
            "Explain your experience in Linux troubleshooting",
            "How do you handle system incidents?",
            "What automation scripts have you written?"
        ]
    }